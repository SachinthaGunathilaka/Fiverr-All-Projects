public class Split {
    int start;
    int end;

    public Split(int start, int end){
        this.start = start;
        this.end = end;
    }

    public Split() {
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
